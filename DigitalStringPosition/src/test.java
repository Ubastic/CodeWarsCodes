
public class test {
	
	public static boolean isConsecutive(String a, String b){
		//if number has an x
		int numA = Integer.parseInt(a);
		int tempNum = numA + 1;
		String checkNum = Integer.toString(tempNum);
		for(int i = 0; i < checkNum.length(); i++)
			if(checkNum.charAt(i) != b.charAt(i) && b.charAt(i) != 'x')
				return false;
		return true;
	}
	
}
